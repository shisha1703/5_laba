def ppp():
    def perevod(number):  # Функция для перевода чисел
        roman_numerals = [  # Список кортежей, содержащий римские символы и их арабские значения
            (1000, "M"),
            (900, "CM"),
            (500, "D"),
            (400, "CD"),
            (100, "C"),
            (90, "XC"),
            (50, "L"),
            (40, "XL"),
            (10, "X"),
            (9, "IX"),
            (5, "V"),
            (4, "IV"),
            (1, "I")
        ]

        result = ""  # Переменная для хранения результата функции
        for arab, roman in roman_numerals:  # Цикл проходящий по каждой паре (число, символ) из списка roman_numerals
            while number >= arab:  # Пока число (number) больше или равно числу (arab)
                result += roman  # Добавляем в переменную результата символ (roman)
                number -= arab  # Вычитаем число из number

                return result  # Возвращаем число результата
            return result  # Возвращаем число результата


    num1 = int(input("Введите число: "))  # Ввод числа пользователя
    num2 = perevod(num1)  # Преобразование числа в римскую с помощью функции
    print(f"Число {num1}: {num2}")